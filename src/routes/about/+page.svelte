  <p>
    This project keeps track of user-reported "close calls" &mdash; near-misses
    between people walking, biking, and driving. We use this data to bring awareness
    to poorly-designed roads and intersections, and to advocate for safer streets
    for everyone. Examples of close calls:
  </p>

  <ul>
    <li>A cyclist almost getting doored</li>
    <li>A pedestrian almost getting hit by a vehicle making a right turn on red</li>
    <li>Almost getting hit due to a blind intersection</li>
    <li>Any other situation in which a collision was narrowly avoided</li>
  </ul>
  
  <p>
    Anyone can anonymously report a close call by visiting the <a href="/report">report form</a>
    and filling it out. Along with being displayed on the public map, we may also
    share the data with local officials so they know where to focus their efforts
    to make the roads safer.
  </p>

  <p>
    If you frequently walk, bike, or roll in your community, we encourage
    you to bookmark the form and report close calls, whether or not you were personally
    involved. Your participation will help us improve road safety in your area!
  </p>

  <p>
    This is a project from <a href="https://eastsideurbanism.org/">Eastside Urbanism</a>.
  </p>

